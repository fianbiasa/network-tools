        }

        async function runCheck() {
            const toolType = document.getElementById('tool-type').value;
            const query = document.getElementById('query-input').value.trim();
            const resultBox = document.getElementById('result-box');
            
            if (!toolType) {
                alert('Please select a tool');
                return;
            }
            
            if (!query) {
                alert('Please enter a domain or IP address');
                return;
            }

            resultBox.className = 'result-box show loading';
            resultBox.innerHTML = '<div class="spinner"></div><span>Processing...</span>';

            try {
                const response = await fetch('api.php', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
                    body: `action=${encodeURIComponent(toolType)}&query=${encodeURIComponent(query)}`
                });
                
                if (!response.ok) throw new Error('Network response was not ok');
                
                const data = await response.json();
                
                resultBox.className = 'result-box show';
                if (data.success) {
                    const resultsHTML = data.data.map(item => `
                        <div class="result-item">
                            <strong>${item.label}:</strong> <span>${item.value}</span>
                        </div>
                    `).join('');
                    resultBox.innerHTML = `<h3>${data.title || 'Results'}</h3>${resultsHTML}`;
                } else {
                    resultBox.innerHTML = `<p class="error">‚ùå ${data.message || 'An error occurred'}</p>`;
                }
            } catch (error) {
                resultBox.className = 'result-box show';
                resultBox.innerHTML = `<p class="error">‚ùå Error: ${error.message || 'Connection failed'}</p>`;
            }
        }

        // Event Listeners
        document.getElementById('query-input').addEventListener('keypress', function(e) {
            if (e.key === 'Enter') runCheck();
        }, { passive: true });

        // Initialize
        updatePlaceholder();
